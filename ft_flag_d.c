#include "ft_printf.h"

int	ft_flag_d(int n)
{
	int		i;
	char	*str;

	i = 	0;
	str = 	ft_itoa(n);
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
